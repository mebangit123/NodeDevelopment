let number = 3;

switch(number)
{
	case 0:
		console.log("Zero");
		break;
	case 1:
		console.log("One");
		break;
	case 2:
		console.log("Two");
		break;
	case 3:
		console.log("Three");
		break;
	case 4:
		console.log("Four");
		break;
	case 5:
		console.log("Five");
		break;
	case 6:
		console.log("Six");
		break;
	default:
		console.log("Enter number between 0 - 6");
}
